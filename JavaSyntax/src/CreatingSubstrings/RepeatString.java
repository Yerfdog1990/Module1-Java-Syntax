package CreatingSubstrings;
/*
String repeat(int count)
Repeats the current string n times
 */
public class RepeatString {
    public static void main(String[] args) {
        String str1 = "Godfrey";
        String str2 = "Okoth";
        String str3 = "Ouma";
        System.out.println("Repeat the string \"Godfrey\" two times: " +str1.repeat(2));
        System.out.println("Repeat the string \"Okoth\" three times: " +str2.repeat(3));
        System.out.println("Repeat the string \"Ouma\" four times: " +str3.repeat(4));
    }
}
