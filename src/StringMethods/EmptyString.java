package StringMethods;
/*
Checks whether the string is an empty string
 */
public class EmptyString {
    public static void main(String[] args) {
        String str1 = "Godfrey Okoth Ouma";
        String str2 = "";
        System.out.println("String 1 has an empty space: " +str1.isEmpty());
        System.out.println("String 2 has an empty space: " +str2.isEmpty());
    }
}
