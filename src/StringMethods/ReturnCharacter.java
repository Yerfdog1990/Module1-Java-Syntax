package StringMethods;
/*
Returns the character at the index position in the string.
 */
public class ReturnCharacter {
    public static void main(String[] args) {
        String str = "Godfrey Okoth Ouma";
        System.out.println("The chacter at index 5 is : " +str.charAt(5));
        System.out.println("The chacter at index 7 is : " +str.charAt(7));
    }
}
