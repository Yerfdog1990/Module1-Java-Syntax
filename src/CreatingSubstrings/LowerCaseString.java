package CreatingSubstrings;
/*
String toLowerCase()
Converts the string to lowercase
 */
public class LowerCaseString {
    public static void main(String[] args) {
        String str1 = "Godfrey Okoth Ouma";
        String str2 = "Cyril Okoth Okoth";
        String str3 = "Cyrek Odhiambo Okoth";
        System.out.println("Convert all characters in string 1 to lower case: " +str1.toLowerCase());
        System.out.println("Convert all characters in string 2 to lower case: " +str2.toLowerCase());
        System.out.println("Convert all characters in string 3 to lower case: " +str3.toLowerCase());
        }
}
