package CreatingSubstrings;
/*
String toUpperCase()
Converts the string to uppercase
 */
public class UpperCaseString {
    public static void main(String[] args) {
        String str1 = "Godfrey Okoth Ouma";
        String str2 = "Cyril Okoth Okoth";
        String str3 = "Cyrek Odhiambo Okoth";
        System.out.println("Convert all characters in string 1 to upper case: " +str1.toUpperCase());
        System.out.println("Convert all characters in string 2 to upper case: " +str2.toUpperCase());
        System.out.println("Convert all characters in string 3 to upper case: " +str3.toUpperCase());
    }
}
